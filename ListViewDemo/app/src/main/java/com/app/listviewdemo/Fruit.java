public class Fruit {
    private String name;
    private int imageid;

    public Fruit(String name, int imageId) {
        this.name = name;
        this.imageid = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageid() {
        return imageid;
    }

}
